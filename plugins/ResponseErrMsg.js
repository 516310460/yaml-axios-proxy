// 响应信息错误提示
const ResponseErrMsg = {
  ErrCodeMsg(code) {
    if (code === 3001) {
      Vue.bus.emit('user-logout')
      return
    }
    // 当前的语言..
    let currentLanguage = JSON.parse(window.localStorage.getItem('buybit')).locale
    switch (currentLanguage) {
      case 'zh-CN':
        common(ErrMsg, code)
        break
      case 'zh-TW':
        common(zhTW, code)
        break
      case 'en-US':
        common(enUS, code)
        break
    }
  }

}

function common(arr, code) {
  let NewVue = new Vue({})
  for (const item of arr) {
    if (code === item.code) {
      NewVue.commonFn.TipsMessage(item.Msg)
    }
  }
}

